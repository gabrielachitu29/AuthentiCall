export function analyzeSmsContent(message: string): SmsAnalysisResult {
  const lowerCaseMessage = message.toLowerCase();
  let isMalicious = false;
  let reason: string | null = null;
  const detectedLinks: string[] = [];

  // Check for phishing keywords
  for (const keyword of PHISHING_KEYWORDS) {
    if (lowerCaseMessage.includes(keyword)) {
      isMalicious = true;
      reason = `Detected phishing keyword: "${keyword}"`;
      break;
    }
  }

  // Check for malicious link patterns
  for (const pattern of MALICIOUS_LINK_PATTERNS) {
    const matches = message.match(pattern);
    if (matches) {
      isMalicious = true;
      reason = reason || 'Detected suspicious link pattern.';
      detectedLinks.push(...matches);
    }
  }

  // Further checks can be added here, e.g.,
  // - Heuristic analysis for suspicious character substitutions (e.g., "goo.gl" vs "g00.gl")
  // - Integration with a real-time URL reputation service (requires API key and network request)
  // - Machine learning model inference (if a lightweight model is available on-device)

  return {
    isMalicious,
    reason,
    detectedLinks: [...new Set(detectedLinks)], // Remove duplicates
  };
}